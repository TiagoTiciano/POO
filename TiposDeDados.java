class TiposDeDados {
    public static void main(String[] args){

        byte contador = 1;
        int anguloEmGraus = -45;
        short indice = 6;
        long numeroPessoasNoMundo = 7000000000L; // L significa que o número é do tipo long

        System.out.println(contador);
        System.out.println(anguloEmGraus);
        System.out.println(indice);
        System.out.println(numeroPessoasNoMundo);

        float media = 7.7776f;
        System.out.println(media);
        media = 9.7777f;
        System.out.println(media);

        double dizima = 5.5555555555555555;
        System.out.println(dizima);
        dizima = 3.23423423455234234234233;
        System.out.println(dizima);

        char letra = 'p';
        System.out.println(letra);
        letra = 'T';
        System.out.println(letra);

        char numero = '1';
        System.out.println(numero);
        numero = '3';
        System.out.println(numero);

        String nome = "Tiago";
        System.out.println(nome);
        nome = "Oliveira";
        System.out.println(nome);

        boolean valor = false;
        System.out.println(valor);
        valor = true;
        System.out.println(valor);
    }
    
}
